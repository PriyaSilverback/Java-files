
//Matchbox analogy. 
//Matchbox represents the variable.
//The value of the variable is what's inside the matchbox

class Tut2_Variables{
    public static void main (String[] args) {
        
        // Variables: variable-type, identifier; assignment-operator(=)
        // int (whole number, no decimals)
        
       
        int a = 20; // value on the right hand side is put into the value on left and is done by assignment operator
   
        System.out.println(a);
        
        // Different types of number variables
        // all variables are signed + or -
         byte b;  // smallest variable type, 8 bits (stores value upto 255)
         short s; // 16 bits
         int i; // 32 bits
         long l; // 64 bits 
         
         // Decimal types
         float f = 3.14f ; // 32 bits float is smaller than doublw
         double d  = 3.14;// 64 bits // you can also put 3.14d just to make sure its a doube variable
                 
         
                  
         
        
    }
    
}